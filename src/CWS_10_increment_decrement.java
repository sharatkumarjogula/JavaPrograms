public class CWS_10_increment_decrement {
    public static void main(String args[]){
        int i = 56;
        System.out.println(i++);// first i value is prined next incremented
        int j= 56;
        System.out.println(++j);// first i value is incremnted then printed

        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int y=7;
        int x=++y*8;
        System.out.println(x);
        int ch='a';
        System.out.println(++ch);

    }
}
